		<p>Dans un affacturage classique, le factor finance les factures clients au fur et à mesure de leur émission. L'affacturage déconsolidant est une forme particulière de factoring dans laquelle toutes les factures en cours sont transmises en une seule fois au factor. Ce type de solution permet en général une amélioration du bilan de la société pour une période spécifique.</p>
		<h2>Explications</h2>
		<p>Le principe du factoring, qu'il soit classique ou déconsolidant, est de confier le financement des factures émises à la clientèle à un factor. Celui-ci avance 90% du montant de la facture dans les 48h, et se charge de récupérer le montant dû auprès de chaque client. On parle ainsi parfois aussi <i>d'externaliser le poste clients</i>. Dans l'affacturage déconsolidant, la totalité (ou partie) du poste clients est transmis en une seule fois au factor. L'ensemble des montants impayés sont alors immédiatement versés par ce dernier à l'entreprise. Cette solution permet:</p>
		<ul>
			<li><b>Une entrée immédiate de liquidités</b>: les montants pouvant s'avérer importants en particulier avec un nombre important de factures en souffrance.</li>
			<li><b>Une amélioration du bilan de la société</b>: en diminuant, lors du bilan, la part de factures "impayées" ou "en attente". Certains ratios ou indicateurs sont ainsi améliorés de manière significative.</li>
		</ul>
		<h2>Une solution à long terme également</h2>
		<p>Le factoring déconsolidant peut aussi, une fois les créances transférées, se poursuivre sous la forme d'un affacturage classique: la gestion du poste clients est ainsi confiée au factor sur la durée. Cette solution permet entre autre:</p>
		<ul>
			<li>De recevoir une avance sur toute facture émise envers la clientèle</li>
			<li>De récupérer plus facilement les montants échus grâce à la cession de créances</li>
			<li>Globalement, de <b>protéger sa société contre le défaut de paiement de la clientèle</b>, grâce à un ensemble de solutions adéquates.
		</ul>
