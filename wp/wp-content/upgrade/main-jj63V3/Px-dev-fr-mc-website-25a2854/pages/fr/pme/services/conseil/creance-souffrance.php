		<p>Vous rencontrez des problèmes de factures clients impayées? Quelles sont les solutions pour y faire face, et qu'entend-on par <i>créance en souffrance</i>? Spécialisés dans les produits financiers pour PME, nous pouvons vous proposer des solutions concrètes.</p>
		<h2>Qu'est-ce qu'une créance en souffrance?</h2>
		<p>De manière simplifée, on associe souvent les créances aux factures envoyées à la clientèle. De manière plus formelle cependant, une créance désigne <b>le droit de réclamer un remboursement auprès de son client</b>. On dit d'une créance qu'elle est <i>en souffrance</i> dès lors que la société n'arrive pas à faire valoir ce droit.</p>
		<h2>Délai de paiement des factures</h2>
		<p>En règle générale, les factures sont à payer dans un délai de 30 jours ou 90 jours. Durant ce délai, la créance est <i>en attente de paiement</i>. Une fois le délai passé, si le créancier n'a toujours pas reçu de paiement de la part du débiteur, on considère alors la créance comme en souffrance. Débute alors la phase de recouvrement, qui peut comporter plus ou moins d'étapes, mais commence par l'envoi de <i>rappels de paiement</i> et peut aller <b>jusqu'aux procédures juridiques</b>.</p>
		<h2>Débiteur et créancier</h2>
		<p>Lorsqu'une société facture un bien ou un service auprès d'un client, elle devient alors la propriétaire d'une créance. Plus spécifiquement:</p>
		<ul>
			<li>La société est alors désignée sous le terme de <b>créancier</b>.</li>
			<li>Le client est alors désigné sous le terme de <b>débiteur</b>.</li>
		</ul>
	
		<h2>Comment protéger son business?</h2>
		<p>Si quelques retards de paiement ne sont pas forcément très dommageables pour une société, de nombreuses factures impayées peuvent mener à véritable manque à gagner. A ceci, il convient d'ajouter <b>le temps et l'énergie nécessaires au recouvrement</b>. Pour cette raison, certaines sociétés recherchent des solutions visant à protéger leurs activités contre le retard de paiement de la clientèle. Ces solutions existent essentiellement sous la forme:</p>
		<ul>
			<li><b>De factoring dit "classique"</b>: qui permet de recevoir une avance sur toute facture émise envers la clientèle. On parle aussi d'affacturage ou financement de factures.</li>
			<li><b>De recouvrement</b>, ou externalisation du recouvrement: solution qui consiste à confier la gestion des impayés à une société spécialisée.</li>
			<li><b>D'assurance-crédit</b>: produit qui consiste à assurer son entreprise contre le risque de non-paiement de la clientèle (suite à une insolvabilité ou un refus de paiement).</li>
			<li><b>De la vente de créances</b>: qui permet de céder des créances en souffrance contre de l'argent cash à un acheteur qui devient alors le nouveau créancier.</li>
		</ul>
