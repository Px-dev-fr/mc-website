		<h2>Fonctionnement en quatre étapes</h2>
		<p>Essentiellement, cette solution pour entreprise se déroule en quatre étapes successives et fait intervenir trois acteurs: le preneur, le bailleur, et le bien à financer.</p>
		<ul>
			<li><b>Première étape</b>: le contrat. Le preneur contacte le bailleur et lui indique quel est le bien à financer. Un contrat est établi.
			<li><b>Seconde étape</b>: le bailleur (banque ou établissement financière) achète alors le bien à financer.</li>
			<li><b>Troisième étape</b>: le bailleur cède l'utilisation du bien au preneur en échange d'un coût mensuel fixe et déterminé à l'avance.</li>
			<li><b>Quatrième étape</b>: en fin de contrat, le preneur peut racheter le bien au prix de sa valeur résiduelle, ou envisager de conclure un nouveau contrat.</li>
		</ul>
