		<h2>Qu'est-ce que l'amortissement hypothécaire?</h2>
		<p>Dans le cas d'une hypothèque, le bien immobilier est financé en partie par le propriétaire, et en partie par la banque. On considère donc qu'une partie du bien "appartient" à la banque. En plus du coût de l'hypothèque (qui varie en fonction du taux d'intérêt, qu'il s'agisse d'un taux fixe ou variable, le propriétaire peut décider de rembourser à la banque une partie du prêt, et donc de "racheter" une partie de sa maison. On appelle cela <b>l'amortissement hypothécaire</b>.</p>
		<h2>Amortissement direct et indirect</h2>
		<p>Il existe deux manières d'amortire son hypothèque: <b>l'amortissement direct</b> et <b>l'amortissement indirect</b>. Chaque méthode possède ses propres avantages. Explications:</p>
		<ul>
			<li><b>Amortissement direct</b>: il s'agit de rembourser la banque. L'avantage est que vous diminuez votre endettement, et donc le prix mensuel de l'hypothèque. Attention au niveau fiscal, les intérêts de la dette étant déductibles de vos impôts.</li>
			<li><b>Amortissement indirect</b>: il s'agit d'un amortissement hypothécaire où vous versez l'argent dans un pilier 3a. Si vous ne diminuez pas directement votre endettement, vous profitez d'un avantage au niveau fiscal.</li>
		</ul>
