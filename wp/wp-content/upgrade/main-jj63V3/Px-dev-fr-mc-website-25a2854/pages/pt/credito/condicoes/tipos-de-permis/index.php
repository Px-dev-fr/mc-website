<p> Para viver na Suíça enquanto estrangeiro durante um determinado tempo, ou permanentemente, precisa de um visto 
(<b>permis de séjour</b> em francês, Ausländer Ausweiss em alemão, permesso di soggiorno em italiano). Este documento oficial mostra as condições do seu estabelecimento na Suíça, a sua duração e se você tem o direito de ter a sua família consigo. </p>
<p> A fim de obter um visto, tem de contactar a autoridade cantonal das migrações do seu local de domicílio. Como cidadão de um país membro da UE/ AELE, aqui tem os três principais tipos de permis.</p>
<h2> Permis L (visto de estadia de curta duração) </h2>
<p> Os titulares de um permis L exercem ou não uma atividade lucrativa e residem na Suíça a curto prazo, em regra geral menos de um ano. As pessoas que apresentam um contrato de trabalho de uma duração de 3 meses a 1 ano têm direito a este visto. O visto também pode ser renovado, após os 12 meses, sem ter de voltar ao seu país de origem. A condição é de dispor de um contrato de trabalho. Neste caso terá acesso ao visto de estadia (permis B).</p>
<p>Se tiver, desde o início, um contrato de trabalho de duração indeterminada ou de mais de 12 meses consecutivos de um patrão, e não de uma agência para trabalhadores temporários, poderá então obter diretamente o permis B. Desta forma terá então mais direitos e sua vida na Suíça será mais fácil.</p>
<h2>Permis B (visto de estadia)</h2>
<p>Os titulares de um permis B exercem ou não uma atividade lucrativa e residem na Suíça com uma finalidade específica. Os estrangeiros que não exercem uma atividade lucrativa têm direito a este visto, com a única condição de provarem que dispõem de meios financeiros suficientes e de um seguro de saúde e acidente cobrindo todos os riscos (AVS-AI).</p>
<p>O permis B dá-lhe a possibilidade de viver na Suíça durante 12 meses ou até 5 anos consoante a sua situação financeira ou o seu contrato de trabalho. Com este visto <b>pode trazer a sua família para residir consigo na Suíça</b>.</p>
<p>Este visto é prolongado por 5 anos se o estrangeiro preenche as condições necessárias. Contudo, o prolongamento pode ser limitado a 1 ano se a pessoa se encontra numa situação de despedimento involuntário durante mais de doze meses consecutivos. Para mais informações sobre o assunto, confira a nossa página sobre o permis B.</p>
<p>Se deseja ficar na Suíça após os 5 anos e se corresponde às condições, poderá então pedir um visto de residência (permis C), equivalente em direitos a um cidadão suíço, simplesmente não permite votar para as eleições federais.</p>
<h2>Permis C (visto de residência)</h2>
<p>Os titulares de um permis C obtêm este visto após uma estadia de 5 ou 10 anos na Suíça. O direito à residência é de duração indeterminada. O visto dá-lhe praticamente os mesmos direitos que os suíços, tirando o direito de voto como mencionado no parágrafo anterior, ou seja, vai-lhe facilitar a vida na Suíça em numerosas situações, tais como:</p>
<ul>
<li>Alugar uma casa mais facilmente</li>
<li>Efetuar créditos ou leasings sem restrições com uma taxa de juros acessível</li>
<li>Entrar e sair da Suíça sem qualquer preocupação</li>
</ul>
