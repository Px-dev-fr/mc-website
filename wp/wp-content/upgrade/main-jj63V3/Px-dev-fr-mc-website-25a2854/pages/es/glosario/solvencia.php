		<p><strong>Solvencia:</strong> La solvencia es la capacidad de una persona a reintegrar o reembolsar un préstamo. Este indicador varía en función de las agencias de crédito, pero en general se evalúa según ciertos criterios.</p>
		<h2>¿Cuáles son los criterios de solvencia?</h2>
		<p>La solvencia de una persona depende por supuesto de su situación financiera, pero no es el único factor que se toma en cuenta a la hora de evaluar la capacidad crediticia de un cliente. En efecto, <strong>la solvencia</strong> depende de:
		<ul>
			<li><b>Los ingresos:</b> en efecto, cuanto más sean los ingresos mensuales de una persona, mayor será su capacidad de solvencia y esta persona será considerada como "buena".</li>
			<li><b>Los gastos:</b> tener un ingreso alto no es suficiente para ser considerado "solvente". También los gastos fijos no deben de ser demasiado elevados en relación con los ingresos. Es precisamente para evaluar la solvencia de un cliente que las agencias de crédito piden regularmente el alquiler mensual que paga el prestatario del crédito.</li>
			<li><b>Estabilidad en el empleo:</b> en general se considera que tiene una mayor capacidad crediticia un empleado que ocupa un cargo desde hace muchos años, en vez de una persona que acaba de cambiar de trabajo.</li>
			<li><b>Situación familiar:</b> este factor poco conocido a priori, pero, sin embargo, muy presente..Un padre de familia, casado y con hijos, es a menudo mejor considerado por los bancos (en el caso de una solicitud de crédito), que una madre soltera, por ejemplo. Por esta razón, las agencias de crédito necesitan saber su situación familiar.</li>
			<li><b>Los antecedentes:</b> lógicamente, una persona que tenía pagos atrasados en créditos personales previos, tendrá menos posibilidades de obtener un crédito, que una persona que nunca ha tenido un problema por falta de pago. Incluso, cabe también señalar que una persona que ya ha realizado y pagado debidamente sus préstamos privados, estará mejor considerada en términos de solvencia, una persona que nunca ha pedido dinero prestado.</li>
		</ul>
		<h2>¿De qué forma mi solvencia va afectar mi solicitud de crédito?</h2>
