		<h2>Eigenkapital - Wo liegt das Problem?</h2>
		<p>Häufig kommen Unternehmen in einen Liquiditätsengpass, da ihre Kunden Rechnungen zu spät oder gar nicht bezahlen. Es fehlt also an Geld, um laufende Ausgaben wie Miete etc. zu bezahlen. Auf Dauer kann dies ein Unternehmen schwer schädigen. Wenn Sie sich in dieser Situation befinden, können Sie <b>per Factoring Ihr Eigenkapital</b> erhöhen:</p>
		<ul>
		<li>Sie schicken Ihrem Kunden eine Rechnung für Warenlieferungen oder Dienstleistungen.</li>
		<li>Einem Schweizer Drittunternehmen, dem <b>Factor</b>, schicken Sie eine Kopie der Rechnung.</li>
		<li>Der Factor wird Ihnen <b>sofort 90%</b> des Rechnungsbetrages überweisen.</li>
		<li>Der Kunde bezahlt daraufhin seine Rechnung nicht bei Ihnen, sondern transparent beim Factor.</li>
		<li>Der Factor überweist Ihnen die restlichen <b>10%</b>, sobald die Rechnung gezahlt wurde.</p>
		</ul>
		<p>Durch dieses System können Sie für Ihr Unternehmen nachhaltig das <b>Eigenkapital erhöhen</b>! Ihre verrechneten Ansprüche werden sofort zu Liquidität und Sie kommen somit schneller zu Ihren Ansprüchen (der Factor verlangt eine Kommission für seinen Dienst).</p>
		<h2>Das Factoring-Paket: Viele Möglichkeiten</h2>
		<p>Factoring umfasst mehrere Dienstleistungen, die alle Ihrem Unternehmen helfen, das Eigenkapital zu erhöhen. Diese Dienstleistungen können Sie einzeln oder als Paket wählen. Ein solches Paket umfasst folgende Produkte:</p>
		<ul>
		<li><b>Rechnungsfinanzierung</b>: Sie erhalten sofort 90% des verrechneten Betrages</li>
