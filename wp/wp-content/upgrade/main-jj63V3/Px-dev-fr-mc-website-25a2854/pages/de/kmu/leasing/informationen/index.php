		<h2>Erklärungen zum Leasing</h2>
		<p>Leasing ist den meisten Leuten heutzutage ein Begriff, aber viele wissen noch nicht, wie attraktiv diese Lösung in ihrem Unternehmen einsetzbar ist. Das Produkt ist ein Zusammenspiel zwischen einem <i>Leasingnehmer</i>, <i>einem Leasinggeber</i> und <i>dem finanzierten Objekt</i>. Genauer gesagt:</p>
		<ul>
			<li><b>Der Leasingnehmer</b> ist ein Unternehmen, das ein Objekt nutzen möchte.</li>
			<li><b>Der Leasinggeber</b> ist eine Bank oder ein Finanzierungsinstitut, die das Objekt anstelle des Leasingnehmers kauft und ihm die Nutzung gegen eine monatliche Rate abtritt.</li>
			<li><b>Das Objekt</b> kann ein Fahrzeug, eine Maschine, ein Werkzeug, etc. sein.</li>
		</ul>
		<p>Anders gesagt wird der Kauf des Objektes dem Leasinggeber überlassen, woraufhin der Leasingnehmer dieses <i>mietet</i>. Die Dauer des Vertrages beträgt normalerweise 36 bis 84 Monate. Sobald der Vertrag endet, kann entweder ein neuer Vertrag für ein anderes Objekt aufgesetzt oder das Objekt kann für seinen Restwert vom Leasingnehmer gekauft werden.</p>
		
		<h2>Informationen zu den Kosten</h2>
		<p>Die monatlichen Kosten dieses Produktes hängen von <b>4 Faktoren</b> ab: Von der Dauer des Leasings, vom Preis des Objektes, vom geschätzten Restwert des Objektes und dem angewandten Zinssatz. Wenn man bedenkt, dass die Monatskosten fix und häufig gering sind, wird das Leasing zu einer besonders interessanten Option, um Produktionsmittel ohne Kapitalblockierung anzuschaffen.<p>
		<p><i>Ein Beispiel</i>: Ein KMU im Maschinenbau will eine neue CNC-Maschine anschaffen. Der Neupreis dieses Objektes beträgt 35'000 CHF. Da der Betriebsleiter sein Investitionskapital sparen möchte, entscheidet er sich für ein Leasing auf 36 Monate. Schätzungsweise beträgt der Restwert des Objektes beim Vertragsende noch 20'000 CHF. Für einen Zinssatz von 3.0% bezahlt das KMU <b>monatlich nur 485 CHF</b>.</p>
		
