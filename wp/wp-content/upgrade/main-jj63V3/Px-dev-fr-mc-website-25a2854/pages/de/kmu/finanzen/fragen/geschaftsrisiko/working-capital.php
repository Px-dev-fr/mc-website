		<p>Was sich hinter dem Begriff "Working Capital" verbirgt, wie Sie es berechnen und gewinnbringend einsetzen können erfahren Sie auf unserer Seite für Unternehmensberatung.</p>
		<h2>Working Capital: Was ist das?</h2>
		<p>Der Begriff 'Working Capital' kommt aus der englischsprachigen Finanzwelt und bezeichnet einfach gesagt die Mittel, die ein KMU für den reibungslosen Geschäftsablauf benötigt. Wir zeigen Ihnen, warum dies so wichtig ist, wie sich das Working Capital berechnen lässt und wie Sie es erhöhen.</p>
		<h2>Für was braucht ein KMU Working Capital?</h2>
		<p>Working Capital wird gebraucht, um kurzfristig Ausgaben zu decken. Man kann auch sagen, dass Working Capital den Liquiditäten des Unternehmens entspricht. Mit diesem Kapital bezahlt ein KMU alle anstehenden Ausgaben, bspw. Miete, Löhne, Leasing, Versicherungen, Anschaffungen, Kredite etc. Sie sehen also, dass es für ein KMU wichtig ist, <b>ausgeglichenes Working Capital</b> zu haben. Das Working Capital berechnet sich wie folgt:</p>
		<ul>
		<li><b>Formel</b>: Verfügbare Mittel / anstehende Ausgaben</li>
		<li><b>Verhältnis <1</b>: Ihr KMU hat nicht genug Kapital, um die Ausgaben auszugleichen</li>
		<li><b>Verhältnis >2</b>: Ihr KMU investiert nicht genügend Kapital</li>
		<li><b>Verhältnis 1.2-2</b>: Ihr KMU hat genügend Mittel, um Ausgaben zu decken und investiert vernünftig</li>
		</ul>
		<h2>Wie erhöhe ich mein Working Capital?</h2>
		<p>Das Working Capital lässt sich durch verschiedene Massnahmen erhöhen, beispielsweise über einen Kredit, Investoren oder durch <b>Factoring</b>. Besonders letzte Variante möchten wir Ihnen hier vorstellen. Beim Factoring arbeiten Sie mit einem Schweizer Drittunternehmen, dem <b>Factor</b>, zusammen, der Ihrem Unternehmen die Verkaufsrechnungen vorfinanziert. Der Factor bezahlt Ihnen <b>90%</b> des Betrages, den Sie Ihren Kunden verrechnet haben, im Voraus! Danach bezahlt der Kunde beim Factor und sobald dieser das Geld erhalten hat, überweist er Ihnen die restlichen <b>10%</b>. Sehen Sie sich unsere <a href="/de/kmu/factoring">Hauptseite über Factoring</a> an, wenn Sie mehr wissen möchten!</p>
