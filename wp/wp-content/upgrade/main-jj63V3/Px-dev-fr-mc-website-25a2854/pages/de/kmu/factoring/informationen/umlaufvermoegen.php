		<h2>Was ist Umlaufvermögen?</h2>
		<p>Ihr Unternehmen braucht Geld, um laufende Ausgaben (Miete, Gehälter, Lieferungen etc.) zu decken. Für die Zahlung dieser Punkte ist das Umlaufvermögen gedacht. Das Umlaufvermögen besteht aus <b>allen Aktiven, die kurzfristig zu Geld gemacht werden können</b>. Dies sind häufig nicht einkassierte Kundenforderungen, Vorräte an Waren und Rohstoffen oder Aktien, Obligationen, Devisen etc. Es gibt normalerweise drei Möglichkeiten, um an Umlaufvermögen zu kommen:</p>
		<ul>
			<li><b>Verkauf von Waren und Dienstleistungen</b>: Dies ist die häufigste Quelle von Umlaufvermögen bzw. von Liquidität. Ihre Kundschaft erhält eine Rechnung für gelieferte Waren oder ausgeführten Service.</li>
			<li><b>Kapital durch KMU-Kredit</b>: Sie erhalten Kapital auf Kredit, welches Sie zuzüglich Zinsen zurückerstatten müssen.</li>
			<li><b>Kapital durch Investoren</b>: Unternehmen und Privatpersonen investieren Geld in Ihr KMU.</li>
		</ul>
		<h2>Liquidität absichern</h2>
		<p>Es gibt für KMU mehrere Produkte, die dazu dienen, das Umlaufkapitals Ihres Unternehmens abzusichern. Die Idee ist es, einen stetigen Liquiditätsfluss zu garantieren. Unter diese Produkte fallen beispielsweise:</p>
		<ul>
			<li><b>Das Factoring</b>: Sie erhalten sofort <b>einen Vorschuss von 90%</b> auf Ihre verschickten Rechnungen. Ein Factor bezahlt Ihnen diesen Vorschuss.</li>
			<li><b>Die Forderungsabtretung</b>: Sie treten Ihre Forderungen an einen Dritten ab, der sie für einen gewissen Betrag kauft (normalerweise unterhalb des verrechneten Betrages). Somit erhalten Sie sofort einen Grossteil Ihrer Ansprüche in Form von Liquidität.</li>
			<li>Die Kreditversicherung</b>: Ihre verrechneten Beträge werden dank diesem Produkt versichert. Wenn ein Kunde definitiv nicht bezahlt, erhalten Sie eine Entschädigung auf den verrechneten Betrag.</li>
			<li><b>Das Inkasso-Outsourcing</b>: Beauftragen Sie einen Dritten damit, die Verwaltung Ihrer Rechnungen zu übernehmen. Dieser wird sich dann mit Ihrer Kundschaft auseinandersetzen, mahnen und Betreibungen einleiten.</li>
		</ul>
		<p>Alternativ können Sie natürlich auch einen <b>KMU-Kredit</b> abschliessen. Dabei handelt es sich aber mehr um ein Produkt zur sofortigen Kapitalbeschaffung als um eine Methode der Absicherung Ihrer verrechneten Beträge.</p>
