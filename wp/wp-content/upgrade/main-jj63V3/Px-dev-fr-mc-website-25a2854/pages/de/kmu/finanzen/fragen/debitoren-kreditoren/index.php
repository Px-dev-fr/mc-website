		<h2>Debitoren- und Kreditorenposition erklärt</h2>
		<p>Debitoren und Kreditoren sind beide die Grundbausteine Ihres Unternehmens. Ein ausbalanciertes Verhältnis zu beiden zu haben ist äusserst wichtig. Die beiden Begriffe werden wie folgt definiert:</p>
		<ul>
		<li><b>Kreditoren</b>: Diese sind Ihre Gläubiger, also Personen, die Geld von Ihnen fordern. In der Praxis sind dies häufig Ihre Lieferanten, doch der Begriff passt auf jeden, dem Sie berechtigterweise Geld schulden.</li>
		<li><b>Debitoren</b>: Diese sind Ihre Schuldner, gegenüber denen Sie berechtigte Forderungen haben. Häufig sind das in der Praxis Ihre Kunden. Diese Position ist mit einem <b>Risiko von Zahlungsausfällen</b> verbunden.</li>
		</ul>
		<h2>Debitorenposition optimieren</h2>
		<p>Offensichtlich ist es wichtig, die Ausgaben für die Kreditoren durch die Einnahmen der Debitoren zu decken. Dies geschieht meistens durch den Verkauf von Waren und Dienstleistungen. Dabei gibt es aber ein Problem: Ihre Kunden bezahlen erst nach 30 Tagen oder noch später, während die Forderungen der Kreditoren nicht warten können. Wir schlagen Ihnen eine <b>Rechnungsfinanzierung</b> vor:</p>
		<ul>
		<li>Sie schicken Ihrem Kunden eine Rechnung für gelieferte Waren oder ausgeführte Dienstleistungen.</li>
		<li>Dem <b>Factor</b>, einem Schweizer Drittunternehmen, schicken Sie eine Kopie der Rechnung, wodurch er Ihnen <b>sofort 90%</b> des Rechnungsbetrages (abzüglich einer Kommission) vorfinanziert!</li>
		<li>Der Kunde bezahlt seine Rechnung dann beim Factor und nicht bei Ihrem KMU.</li>
		<li>Der Factor überweist Ihnen die restlichen <b>10%</b>, sobald der Kunde bezahlt hat.</li>
		</ul>
