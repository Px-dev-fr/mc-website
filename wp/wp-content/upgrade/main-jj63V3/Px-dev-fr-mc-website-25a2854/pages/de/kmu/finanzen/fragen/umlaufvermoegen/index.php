		<p>Das Bedürfnis nach Umlaufvermögen ist ein wichtiger Hinweis auf den kurzfristigen finanziellen Zustand eines Unternehmens. Das Umlaufvermögen setzt sich aus den kurzfristigen Einkünften und Ausgaben zusammen. Dieses Verhältnis wird als WCR (Working Capital Requirement) angegeben; über diesen Indikator lassen sich die Bedürfnisse und Probleme des Unternehmens auf kurze Frist besser bestimmen.</p>
		<h2>Das WCR berechnen</h2>
		<p>Das Bedürfniss nach Umlaufvermögen (WCR) kann als Differenz zwischen den kurzfristigen Aktiven und Passiven verstanden werden. Also:</p>
		<p style="text-align:center;font-weight:bold;">WCR = Kurzfristige Forderungen - Kurzfristige Verbindlichkeiten</p>
		<p>Forderungen und Verbindlichkeiten entsprechen den voraussichtlichen Einnahmen und Ausgaben innerhalb eines (kurzen) Zeitraumes:</p>
		<ul>
			<li><b>Kurzfristige Forderungen</b>: Bezeichnet alle noch nicht einkassierten Ansprüche - Unbezahlte Kundenrechnungen, Warenlager, eventuell andere Elemente (Devisenreserve, etc.)</li>
			<li><b>Kurzfristige Verbindlichkeiten</b>: Bezeichnet alle Schulden, deren Begleichung kurz bevor steht - Rechnungen von Lieferanten, Gesellschaften (bspw. Versicherungsprämien) oder Insitutionen (bspw. Steuern).</li>
		</ul>
		
		<h2>Lösungen für WCR-Probleme</h2>
		<p>Viele Selbstständige und Unternehmer wissen nicht, dass es viele Finanzprodukte gibt, die KMU dabei helfen, ihr Umlaufvermögen bzw. das WCR zu verbessern. Diese flexiblen Lösungen betragen unterschiedliche Bezeichnungen:</p>
		<ul>
			<li><b>Factoring</b>: Dies ermöglicht es, die Frist zwischen Rechnungssendung und Einkassieren zu minimieren.</li>
			<li><b>Inkasso-Outsourcing</b>: Ein spezialisiertes Unternehmen kann sich für Sie um den Forderungseinzug kümmern.</li>
			<li><b>Reverse-Factoring</b>: Ermöglicht es, Einkaufspreise mit seinen Lieferanten besser zu verhandeln.</li>
			<li><b>Forderungsverkauf</b>: Erhöht das Umlaufvermögen sofort.</li>
			<li><b>KMU-Kredit</b>: Deckt einen kurzfristig erhöhten Liquiditätsbedarf ab.</li>
			<li><b>KMU-Leasing</b>: Ermöglicht die Anschaffung von Produktionsmitteln, ohne das eigene Kapital zu benutzen.</li>
			<li>... Es gibt auf dem Markt zahlreiche verschiedene Produkte, die das Umlaufvermögen eines Unternehmens verbessern können.</li>
		</ul>
		
