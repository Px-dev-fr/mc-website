		<h2>Welche Probleme behebt die Vorfinanzierung?</h2>
		<p>Beim Verkauf von Produkten und Dienstleistungen können sich Ihre Kunden bis zu 30 Tage Zeit lassen, um zu bezahlen. In dieser Zeit müssen Sie aber zahlreiche Ausgaben bezahlen: Miete, Löhne, Lieferungen, Versicherungen etc. Ausserdem tragen Sie das Risiko, dass Ihr Kunde nicht zahlt. Vorfinanzierung hilft Unternehmen bei diesen Problemen in vielfältiger Weise:</p>
		<ul>
			<li><b>Liquiditätsfluss</b>: Ihre Verkaufsrechnungen werden sofort zu Geld, wodurch Sie stetig Kapital erhalten</li>
			<li><b>Vermindertes Risiko</b>: Ihre Rechnungen werden beglichen, auch wenn der Kunde insolvent sein sollte.</li>
			<li><b>Mehr Spielraum</b>: Dank einem stetigen Liquiditätsfluss haben Sie mehr Möglichkeiten, Ihr Geld zu investieren oder können magere Zeiten überdauern.</li>
			<li><b>Mehr Sparpotential</b>: Da Sie sofort Kapital erhalten, können Sie eher einen Teil sparen und sich so eine Sicherheit schaffen.</li>
		</ul>
		<h2>Vorfinanzierung und Factoring: Wie funktioniert es?</h2>
		<p>Im neuartigen Produkt <i>Factoring</i> spielt die Vorfinanzierung für Unternehmen eine zentrale Rolle. Dabei arbeiten Sie eng mit einem <i>Factor</i> zusammen, einem Schweizer Drittunternehmen, das für Sie Rechnungen vorfinanziert:</p>
		<ul>
			<li>Sie senden dem Factor eine <b>Kopie der Rechnung</b>, sobald Sie Ihrem Kunden eine solche ausstellen.</li>
			<li>Der Factor überweist Ihnen einen Vorschuss von <b>90%</b> des Rechnungsbetrages innerhalb von <b>48 Stunden</b>.</li>
			<li>Die Rechnung wird dann vom Kunden beim Factor bezahlt.</li>
			<li>sobald der Factor den kompletten Rechnungsbetrag erhalten hat, überweist er Ihnen die restlichen <b>10%</b>.</li>
		</ul>
