		<h1>FAQ: domande frequenti</h1>
		<h3>1. Quali documenti servono per fare una domanda per un credito?</h3>
		<p>Per richiedere un credito, è necessario fornire le ultime 3 buste paga e una fotocopia fronte-retro di un documento d'identità (carta d'identità svizzera, permesso di lavoro B, C, L, G o carta di legittimazione). Per gli indipendenti, inoltre, è necessaria la copia dell'ultimo avviso di tassazione.</p>
		<h3>2. Bisogna vivere in Svizzera per poter ottenere un credito?</h3>
		<p>I crediti sono generalmente concessi ai residenti in Svizzera e ai frontalieri che lavorano in Svizzera.</p>
		<h3>3. Ho già un contratto di credito in corso, è possibile avere un supplemento di credito?</h3>
		<p>Sì, è possibile ottenere un prestito anche se ne avete già un altro in corso. Questo tipo di prestito è quello che viene chiamato un riacquisto del credito.</p>
		<h3>4. Devo giustificare il motivo per il quale chiedo un credito?</h3>
		<p>No, assolutamente no. Siete liberi di fare quello che volete con la somma richiesta.</p>
		<h3>5. Qual è l'importo massimo che posso ottenere?</h3>
		<p>L'ammontare del credito dipende dal reddito e dalla vostra situazione (stato civile e numero dei figli) e varia quindi da persona a persona.</p>
		<h3>6. Quanto tempo ci vuole per ricevere il denaro sul mio conto?</h3>
		<p>Dal momento in cui la richiesta viene accettata, l'importo dovrebbe generalmente essere versato immediatamente dopo la scadenza legale di revoca, o dopo 7 giorni.</p>
		<h3>7. Se perdo il lavoro, devo continuare a pagare?</h3>
		<p>No, i nostri crediti includono un'assicurazione in caso di perdita dell'impiego. Se siete stati licenziati senza motivo (disoccupazione involontaria), l'assicurazione assumerà le rate mensili al vostro posto.</p>
		<h3>8. Faccio fatica a pagare una mensilità, che cosa devo fare?</h3>
		<p>Può capitare a tutti di avere delle difficoltà a pagare una mensilità. In questo caso prendete contatto con noi per avvertirci, troveremo una soluzione per voi.</p>
		<h3>9. Qual è lo stipendio mensile minimo per poter ottenere un credito?</h3>
		<p>Lo stipendio netto mensile deve essere di almeno 2'900 Chf. Se l'importo è inferiore a questa cifra un credito non è consigliato perché potrebbe provocare un indebitamento eccessivo.</p>
		<h3>10. Ho procedimenti giudiziari in corso, posso ancora ottenere credito?</h3>
		<p>No, non avere dei procedimenti giudiziari in corso è una condizione per poter ottenere un credito. Tuttavia, siamo in grado di offrire soluzioni di cancellazione del debito.</p>
		<h3>11. Ho avuto dei procedimenti giudiziari nel passato, ma attualmente rimborsati. Posso ottenere un prestito?</h3>
		<p>Questo dipende dalla quantità dei vostri procedimenti e se si tratta di problemi recenti o passati. La cosa migliore da fare è quella di contattarci, potremo consigliarvi al meglio.</p>
		<h3>12. Posso pagare più mensilità in una sola volta?</h3>
		<p>Sì, naturalmente. Si può tranquillamente rimborsare più rate in una sola volta. Quando tutto il credito viene rimborsato, facciamo una detrazione e vi versiamo gli interessi pagati in eccedenza.</p>
