		<p>Quando um banco analisa um dossiê de pedido de crédito, ele estuda detalhadamente a situação do pedinte. Este estudo dá como resultado um "score" (ou scoring), que indica a situação geral do mutuário e <b>determina se será o seu pedido aceito ou não</b>, assim como as condições sob as quais o crédito será outorgado.</p>
		<h2>Como se calcula o scoring?</h2>
		<p>Cada banco possui seu próprio método de cálculo e sua própria escala. Este indicador toma em conta não somente as receitas do mutuário, mas também várias outras variáveis que possam indicar risco, ou falta dele, em caso de outorgação de crédito. Dessa forma, o cálculo do scoring toma em conta os seguintes elementos:</p>
		<ul>
			<li>A renda, combinada àquela do/da cônjuge, se houver.</li>
			<li>O aluguel pago (ou os encargos hipotecários, caso estejamos lidando com um proprietário)</li>
			<li>Os seguros de saúde</li>
			<li>A localidade (visto que o custo de vida varia de uma região a outra)</li>
			<li>A situação familiar (estado civil, número de crianças e suas idades)</li>
			<li>O tipo de emprego e a estabilidade do emprego (desde quando trabalha para o mesmo empregador)</li>
			<li>A nacionalidade e/ou o visto de trabalho</li>
			<li>A idade</li>
			<li>Antecedentes ou códigos ZEK (centro de informações sobre crédito)</li>
			<li>Etc.</li>
		</ul>
		<h2>Qual é a sua influência?</h2>
		<p>O scoring é, finalmente, um simples indicador que reflete o nível de risco que toma o banco ao lhe outorgar um crédito. É este nível de risco que determinará se será o seu <b>pedido aceito ou não</b>. Dessa forma:</p>
		<ul>
			<li>Um scoring <b>muito baixo</b> levará a uma recusa: o pedido de crédito será considerado muito arriscado.</li>
			<li>Um scoring <b>baixo</b> implicará condições de empréstimo menos vantajosas.</li>
			<li>Um scoring <b>elevado</b> implicará as melhores condições de empréstimo.</li>
		</ul>
		<h2>Como conhecer seu scoring?</h2>
		<p>O scoring calculado pelos bancos para um pedido de crédito não é comunicado: nem aos clientes, nem aos potenciais intermediários. No entanto, vale salientar que, além dos bancos, outros organismos também propõem um indicador de risco. É o caso de sociedades como a "Criff", especializada na coleta de dados e na realização de "scores", utilizados tanto para o estudo de um perfil de empréstimo como para outras coisas.</p>
