		<p>Wenn eine Bank ein Dossier aufgrund einer Kreditanfrage studiert, interessiert sie sich vor allem für die Situation des Kunden. Das Resultat dieser Analyse ist das sogenannte Scoring. Dieser Indikator spiegelt die allgemeine Situation des Konsumenten wider und bestimmt, ob der Kredit gewährt und zu welchen Konditionen er vergeben wird.</p>

		<h2>Wie berechnet man das Scoring?</h2>
		<p>Jede Bank hat ihre eigene Methode, um das Scoring zu berechnen. Dieser Indikator beinhaltet die Einkünfte des Antragsstellers, aber auch zahlreiche andere Variabeln, damit das Risiko einer Kreditvergabe so genau wie möglich dargestellt werden kann. Die Berechnung des Scorings stützt sich auf zahlreiche Angaben, beispielweise:</p>
		<ul>
		<li>Das Einkommen, eventuell ergänzt durch dasjenige des Partners/der Partnerin.</li>
		<li>Die bezahlte Miete (oder Hypothekarkosten für Eigenheimbesitzer)</li>
		<li>Krankenversicherung</li>
		<li>Wohnort (die Lebenskosten variieren von Kanton zu Kanton)</li>
		<li>Familiensituation (Zivilstand, Anzahl Kinder, Alter der Kinder)</li>
		<li>Anstellungsart und -sicherheit (seit wann beim gleichen Arbeitsgeber)</li>
		<li>Nationalität und / oder Arbeitsbewilligung</li>
		<li>Alter</li>
		<li>Negative Zahlungshistorie oder ZEK-Codes</li>
		<li>Etc.</li>
		</ul>
		<h2>Welcher Einfluss hat das Scoring?</h2>
		<p>Scoring ist schlussendlich ein Indikator, der das Risikoniveau einer Kreditvergabe für die Bank wiedergibt. Darum:</p>
		<ul>
		<li>Ein <b>zu niedriges Scoring</b> bedeutet eine Ablehnung: Die Kreditvergabe ist zu risikoreich.</li>
		<li>Ein <b>niedriges Scoring</b> ermöglicht die Kreditvergabe, doch die Konditionen sind schlechter.</li>
		<li>Ein <b>hohes Scoring</b> ermöglicht es, von einem attraktiven Zinssatz zu profitieren</li>
		</ul>
		<h2>Wie erfahre ich mein Scoring?</h2>
