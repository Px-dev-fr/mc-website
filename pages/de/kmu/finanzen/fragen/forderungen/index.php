		<p>Haben Sie ein Problem mit unbezahlten Kundenrechnungen? Wie kann man das Entstehen von Aussenständen vermeiden und was bedeutet <i>ausstehende Forderungen</i> eigentlich? Da wir uns auf Finanzprodukte für KMU spezialisiert haben, können wir Ihnen konkrete Lösungen anbieten.</p>
		
		<h2>Was ist eine ausstehende Forderung?</h2>
		<p>Häufig versteht man unter einer ausstehenden Forderung von Unternehmerseite her Rechnungen, die der Kundschaft geschickt wurden. Formeller ausgedrückt bezeichnet eine Forderung <b>das Recht, von einem Kunden eine Bezahlung zu erhalten</b>. Eine Forderung ist <i>ausstehend</i>, solange das Unternehmen dieses Recht nicht durchsetzen kann.</p>

		<h2>Zahlungsfristen von Rechnungen</h2>
		<p>Generell gesagt sind Rechnungen innerhalb von 30 bis 90 Tagen zu bezahlen. Während dieser Zeit ist der Rechnungssteller in der <i>Erwartung einer Zahlung</i>. Sobald die Frist verstrichen ist und der Gläubiger noch immer keine Zahlung vom Debitor erhalten hat, spricht man von einer ausstehenden Forderung. Daraufhin beginnt der Inkasso-Prozess, der aus mehr oder weniger vielen Schritten besteht, jedoch immer mit dem Sendung von <i>Mahnschreiben</i> beginnt und bis zu <i>rechtlichen Schritten</i> führen kann. </p>
		
		<h2>Debitor und Gläubiger</h2>
		<p>Wenn ein Unternehmen einem Kunden eine Ware liefert oder einen Service ausführt, wird es Eigentümer einer Forderung. Genauer gesagt:</p>
		<ul>
			<li>Tritt das Unternehmen in die Rolle eines <b>Gläubigers</b>.</li>
			<li>Tritt der Kunde in die Rolle eines <b>Debitors</b>.</li>
		</ul>
	
		<h2>Wie sein Business schützen?</h2>
		<p>Einige Zahlungsverspätungen sind für ein Unternehmen meistens nicht sehr schlimm, viele ausstehende Forderungen können aber zu grösseren Verlusten führen. Ausserdem muss man zu diesen Verlusten auch <b>die Zeit und die Energie für Inkasso-Massnahmen</b> hinzurechnen. Deshalb suchen Unternehmen nach Lösungen, um ihre Firma vor Verlusten aufgrund ausstehender Forderungen zu schützen. Diese Lösungen existieren in verschiedenen Formen:</p>
		<ul>
			<li><b>Das "klassische" Factoring</b>: Dies ermöglicht es, einen Vorschuss auf jede der Kundschaft ausgestellten Rechnung zu erhalten. Dabei spricht man auch von Rechnungsvorfinanzierung.</li>
			<li><b>Inkasso-Outsourcing</b>: Dabei übertragen Sie den Einzug von ausstehenden Forderungen an ein spezialisiertes Unternehmen.</li>
			<li><b>Kreditversicherung</b>: Dieses Produkt schützt das Unternehmen vor dem Zahlungsausfallrisiko der Kundschaft (bspw. aufgrund von Insolvenz oder einer Zahlungsverweigerung).</li>
			<li><b>Forderungsverkauf</b>: Diese Technik besteht darin, seine Forderungen gegen eine Barzahlung an einen Käufer abzutreten, der der neue Gläubiger wird.</li>
		</ul>
		
