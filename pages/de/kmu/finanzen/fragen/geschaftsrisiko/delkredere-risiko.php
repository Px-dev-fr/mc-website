		<h2>Delkredere Risiko: Was ist das und wie schütze ich mein KMU?</h2>
		<p>Beim Verkauf von Waren und Dienstleistungen geht ein KMU immer ein Risiko ein, nämlich dass der Kunde nicht oder verspätet bezahlt. Dies wird in der Fachsprache Delkredere Risiko genannt. Der Begriff kommt vom Italienischen <i>del credere</i>, was "des Glaubens" bedeutet. Der Verkäufer "glaubt" also daran, dass der Käufer ihn bezahlt und geht somit ein Risiko ein.</p>
		<h2>Weit verbreitet und nicht zu unterschätzen</h2>
		<p>Als Leiter eines KMU oder als Selbstständiger beziehen Sie wahrscheinlich den Grossteil Ihrer Einkünfte aus dem Verkauf von Waren und Dienstleistungen. Ihre Kunden haben normalerweise <b>30 Tage oder länger</b> Zeit, um Ihre Forderungen zu begleichen. In dieser Zeit gehen Sie bei jedem Geschäft ein Risiko ein, denn:</p>
		<ul>
		<li>Vielleicht <b>bezahlt der Kunde gar nicht</b></li>
		<li>Oder <b>der Kunde bezahlt zu spät</b></li>
		</ul>
		<p>Dieses Delkredere Risiko ist nicht zu unterschätzen, denn wenn der Kunde nicht bezahlen kann, ist es sehr umständlich, doch noch an Ihre Forderung zu kommen und im Äussersten endet der Fall vor dem Richter. Wenn der Kunde zu spät bezahlt, fehlt es Ihnen an Liquidität auf kurze Frist, um laufende Ausgaben zu begleichen. Eine längere Periode von solchen Ausfällen kann ein KMU <b>schwer schädigen</b>. Wie Sie sehen, kann es sich lohnen, das Delkredere Risiko zu minimieren.</p>
		<h2>Wie schütze ich mein KMU?</h2> 
		<p>Es gibt zahlreiche Produkte auf dem Schweizer Finanzmarkt, die das Delkredere Risiko minimieren sollen. Wir empfehlen Ihnen:</p>
		<ul>
		<li><b><a href="/de/kmu/factoring/versicherung">Kreditversicherung</a></b>: Ihre Rechnungsbeträge sind versichert. Sie werden also entschädigt, falls der Kunde nicht bezahlt. Die Kosten liegen zwischen 0.1% und 1% des versicherten Betrages (je nach KMU).</li>
		<li><b><a href="/de/kmu/factoring/klassisches/rechnungsfinanzierung">Rechnungsfinanzierung</a></b>: Sie erhalten <b>90%</b> Ihrer Forderung im Voraus, egal ob der Kunde bezahlt. Dies schaltet das Risiko nicht aus, doch es minimiert es massiv.</li>
		<li><b><a href="/de/kmu/factoring/forderungsabtretung/">Forderungsabtretung</a></b>: Verkaufen Sie Ihre Forderungen und erhalten Sie so den Grossteil Ihrer Einkünfte aus dem Verkauf. Sie verkaufen dabei auch das Delkredere Risiko, was ein doppelter Vorteil ist.</li>
		<li><b><a href="/de/kmu/factoring/inkasso/">Inkasso Outsourcing</a></b>: Überlassen Sie Ihr Inkasso einem Spezialisten. Er ist geübt darin, Forderungen einzutreiben und Sie kommen dadurch eher und schneller an Ihre Einkünfte.</li>
		</ul>
