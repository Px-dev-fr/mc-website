		<p>Das Geschäftsrisiko, auch manchmal als <i>Unternehmerrisiko</i> bezeichnet, bedeutet das Risiko eines Verlustes von Kapital bei einer Geschäftsabwicklung. Es kommt bei allen Unternehmen vor, ist sogar allgegenwärtig. Deshalb ist es attraktiv, dieses Risiko zu minimieren.</p>
		<h2>Geschäftsrisiko: Sie sind betroffen!</h2>
		<p>Sobald Sie an einen Kunden Waren verkaufen oder Dienstleistungen ausführen, gehen Sie ein Risiko ein. Es kann sein, dass der Kunde nicht bezahlen kann oder will. In diesem Fall ist Ihr Kapital in Form von Waren oder Dienstleistungen gefährdet, denn es kann sein, dass Sie für Ihren Aufwand nicht entschädigt werden. Das Geschäftsrisiko betrifft also unter anderem zwei Fälle:</p>
		<ul>
		<li><b>Der Kunde zahlt nicht</b>: Aus irgendwelchen Gründen verweigert der Kunde die Zahlung oder er ist insolvent und kann nicht zahlen.</li>
		<li><b>Der Kunde zahlt verspätet</b>: Ihr Kunde bezahlt, doch erst nach einer nicht näher definierten, längeren Frist.</li>
		</ul>
		<h2>Das Risiko minimieren</h2>
		<p>Bei jedem Geschäft gehen Sie also dieses Risiko ein. Wenn Sie viele Kunden im <b>B2B-Bereich</b> haben, ist es eine attraktive Alternative, einen Dritten mit der Risikominimierung zu beauftragen. Konkret kommt ein sogenannter <b>Factor</b> ins Spiel, der mit seinen <a href="/de/kmu/factoring/klassisches/factoring-schweiz">Factoring-Dienstleistungen</a> Ihr Geschäft absichert:</p>
		<ul>
		<li><b>Kreditversicherung</b>: Der Factor versichert Ihre Rechnungsbeträge. Wenn ein Kunde nicht zahlen kann oder sich weigert, erhalten Sie von ihm eine Entschädigung in Höhe der versicherten Rechnung.</li>
		<li><b>Inkasso Outsourcing</b>: Beauftragen Sie den Factor damit, Ihre Rechnungen zu verwalten. Er übernimmt auch die Mahnung und Betreibung sowie die einvernehmliche Einigungen mit Ihren Kunden.</li>
		<li><b>Risikoanalyse</b>: Der Factor liefert Ihnen wertvolle Informationen über Ihre Kunden: Zahlungsmoral, vergangene Konkurse, finanzielle Situation, Bonität etc.</li>
		</ul>
		<p>Wenn Sie eines dieser Produkte wählen, erhalten Sie einen <b>effektiven Schutz</b> gegen den Kapitalverlust aufgrund von Aufträgen. Wenn Sie die Kreditversicherung und das Inkasso Outsourcing wählen, entspricht dies einem <a href="/de/glossar-kmu/maturity-factoring">Maturity Factoring</a>, einer kompletten Absicherung gegen den Debitorenverlust.</p>
