		<ul>
			<li><b>Preis des Objektes</b>: Je teurer dieses ist, desto höher wird die Leasingrate. Ein Leasing kann für jedes Objekt Ihrer Wahl abgeschlossen werden, der Preis ist zweitrangig.</li>
			<li><b>Zinssatz</b>: Je höher dieser ist, desto teurer wird Ihr Leasing. Multicrédit garantiert Ihnen jedoch den besten Zinssatz aufgrund Ihrer Situation!</li>
