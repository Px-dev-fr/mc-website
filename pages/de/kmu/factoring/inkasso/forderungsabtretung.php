		<h2>Forderungsabtretung: Was ist das?</h2>
		<p>Unternehmen kommen häufig über den Verkauf von Waren oder Dienstleistungen zu Liquiditität bzw. Einkünften. Dabei sind sie aber von der Zahlungsmoral ihrer Kunden abhängig. Was passiert, wenn Ihr Kunde nicht bezahlen kann, bspw. aufgrund von Insolvenz? Mit einer <b>Forderungsabtretung</b> über einen sogenannten <b>Factor</b>, ein Schweizer Drittunternehmen, können Sie dieses Risiko ausschalten:</p>
		<ul>
			<li>Sie "verkaufen" die Forderung an den Factor. Dieser wird Ihnen im Gegenzug sofort einen gewissen Betrag überweisen (normalerweise liegt dieser unter dem Rechnungsbetrag).</li>
			<li>Der Factor ist jetzt der neue Gläubiger Ihres Kunden. Sie müssen sich nicht mehr um die ihm ausgestellte Rechnung kümmern.</li>
			<li>Der Factor kümmert sich selber um die Eintreibung und Verwaltung seiner neuen Forderung.</li>
			<li>Die Forderungsabtretung ist eine <a href="https://www.multicredit.ch/de/kmu/factoring/klassisches">Alternative zum klassischen Factoring</a></li>
		</ul>
		<h2>Die Vorteile einer Forderungsabtretung</h2>
		<p>Eine Forderungsabtretung bringt für Sie zahlreiche Vorteile und ist somit die perfekte Alternative zum Factoring. Einen Factor als Geschäftspartner zu haben kann sich für Ihr Unternehmen als sehr nützlich erweisen. Die attraktivsten Punkte dieser Finanzierungsmöglichkeit sind:</p>
		<ul>
			<li><b>Sofortige Liquidität</b>: Sie verkaufen Ihre Forderungen an den Factor, der Sie innerhalb von <b>48h</b> bezahlt. Sie müssen also nicht die Zahlung Ihrer Kunden abwarten.</li>
			<li><b>Kein Risiko mehr</b>: Da Sie Ihre Forderung abgetreten haben, tragen Sie das Risiko einer Zahlungsunfähigkeit Ihres Kunden nicht mehr, denn der Factor ist der neue Gläubiger. Somit schützen Sie Ihre Finanzen!</li>
			<li><b>Inkasso nicht mehr nötig</b>: Da Sie Ihre Forderung verkauft haben, müssen Sie Ihren Kunden nicht mehr mahnen oder sogar betreiben. Der Factor kümmert sich um die Verwaltung seiner neuen Forderung.</li>
		</ul>
