		<h2>Was ist der Cashflow?</h2>
		<p>"Cashflow" bezeichnet den Ein- und Abfluss von Liquidität in Ihrem Unternehmen. Ein <b>positiver Cashflow</b> bedeutet, dass die Firma echten Gewinn in Form von liquiden Mitteln abwirft. Es ist deshalb für jedes KMU sehr wichtig, diesen Indikator zu optimieren.</p>
		
		<p>Ihren ungefähren Liquiditätsfluss berechnen Sie wie folgt:</p>
		<ul>
			<li>Berechnen Sie Ihren Gesamtgewinn über ein ganzes Jahr.</li>
			<li>Ziehen Sie von diesem alle Aktiven ab, die nicht zahlungsrelevant sind (bspw. ausstehende Forderungen).</li>
			<li>Zählen Sie zu diesem alle Passiven hinzu, die nicht zahlungsrelevant sind (bspw. Abschreibungen, Rückstellungen).</li>
			<li>Dieser <b>bereinigte Gewinn</b> besteht nun aus liquiden Mitteln und gibt den Cashflow an. Solange dieser positiv ist, befinden Sie sich im grünen Bereich.</li>
		</ul>
		
		<h2>Cashflow verbessern: Factoring ist die Lösung</h2>
		<p>Beim Factoring geht es darum, Ihre Kundenforderung durch den Factor, einen Geschäftspartner, vorzufinanzieren. Dadurch <b>fliessen Ihre Einkünfte schneller</b> und Sie werden garantiert bezahlt. Das Produkt funktioniert wie folgt:</p>
		<ul>
			<li>Sie berechnen Ihrem Kunden einen Verkauf oder eine Dienstleistung. Dem Factor schicken Sie davon eine Kopie.</li>
			<li>Ihr Factor überweist Ihnen einen Vorschuss von <b>90% des Rechnungsbetrages</b> innerhalb von 48 Stunden.</li>
			<li>Der Kunde bezahlt die Rechnung daraufhin beim Factor. Dies geschieht automatisch über Ihren Einzahlungsschein.</li>
			<li>Sobald der Factor bezahlt wurde, überweist er Ihnen die restlichen <b>10%</b>.</li>
		</ul>

		<p>Factoring ist vor allem für Unternehmen geeignet, die im B2B-Bereich arbeiten, B2C-Lösungen sind jedoch auch möglich.</p>
		
