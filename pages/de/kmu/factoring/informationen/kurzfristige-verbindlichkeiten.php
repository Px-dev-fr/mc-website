		<p>Der Begriff "Kurzfristige Verbindlichkeiten" ist ein fundamentaler Begriff in der Unternehmensökonomie, doch er ist auch sehr spezifisch und technisch. Was bedeutet dieser Ausdruck einfach gesagt? Wir zeigen Ihnen, was sich hinter der Bezeichnung verbirgt.</p>
		<h2>Kurzfristige Verbindlichkeiten = Schulden</h2>
		<p>Grundsätzlich handelt es sich bei diesem Begriff um gewisse Schulden eines Unternehmens. Insbesondere bedeutet dies alle Verpflichtungen, die das Unternehmen innerhalb eines relativ kurzen Zeitraumes begleichen muss. Es handelt sich also um alle Ausgaben, die kurzfristig getätigt werden müssen, um diese Schulden zu begleichen. Dabei handelt es sich beispielsweise um:</p>
		<ul>
			<li><b>Forderungen</b>: Alle Forderungen, die innerhalb kurzer Zeit bezahlt werden müssen, fallen in diese Kategorie. Dabei handelt es sich beispielsweise um Rechnungen, die Ihre Lieferanten Ihnen schicken.</li>
			<li><b>Laufende Ausgaben und Nebenkosten</b>: Regelmässig zu zahlende Beträge, beispielsweise für Versicherungen, Kredite, Löhne, Miete, Informatik etc.</li>
			<li><b>Administrative Kosten</b>: Alle Schulden bei Institutionen, beispielsweise Steuern, Zollgebühren, Sozialversicherungen etc.</li>
		</ul>
		<h2>Reverse-Factoring zur Optimierung</h2>
		<p>Kurzfristige Verbindlichkeiten müssen schnell bezahlt werden, weshalb es wichtig ist, dass ein Unternehmen über genügend Liquidität verfügt, um genau dies zu tun. Mit der Liquiditätsabsicherung und -beschaffung beschäftigt sich ein ganzer Wirtschaftszweig, der <a href="/de/kmu/factoring/">Factoring</a> genannt wird. Wir bieten stellen Ihnen hier ein Angebot vor, dass <b>Reverse Factoring</b> genannt wird und Ihre Lieferantenkosten sofort begleicht:</p>
		<ul>
			<li>Sobald Sie Waren oder Dienstleistungen von Ihren Lieferanten beziehen, schicken Sie dem <b>Factor</b>, einem Schweizer Drittunternehmen, eine Kopie der Rechnung.</li>
			<li>Der Factor bezahlt die Forderung des Lieferanten an Ihrer Stelle <b>sofort</b> gegen eine Kommission abhängig vom Rechnungsbetrag.</li>
			<li>Sie bezahlen die vorfinanzierte Rechnung danach beim Factor und profitieren von einer <b>grosszügigeren Zahlungsfrist</b>.</li>
			<li>Dadurch <b>sparen Sie Zeit</b>, denn die Zahlungsfrist beim Factor ist normalerweise länger als bei Ihren Lieferanten. Ausserdem <b>profitieren Sie von Skonto und Verhandlungsspielraum</b>, da Ihren Lieferanten die Bezahlung garantiert wird und Sie darum bessere Einkaufspreise aushandeln können.</li>
		</ul>
		<p>Das Reverse Factoring wird <a href="/de/kmu/factoring/reversed/">hier</a> genauer dargestellt.</p>
