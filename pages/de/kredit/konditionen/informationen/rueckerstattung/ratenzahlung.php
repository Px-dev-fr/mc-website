	<h2>Woraus setzt sich ein Kredit zusammen?</h2>
	<p>Für jeden ausgeliehenen Kreditbetrag wird eine gewisse Anzahl an Ratenzahlungen fällig. Sobald alle Raten bezahlt wurden, haben Sie den Kredit komplett abbezahlt und sind schuldenfrei. Es reicht aber nicht, nur den Kredit zurückzuzahlen - Zusätzlich zum geliehenen Geld werden Zinsen fällig, die schlussendlichen Mehrkosten des Kredits. <b>Jede Monatsrate enthält gleichzeitg einen Teil der Rückzahlung sowie der Zinsen</b>. Ein Privatkredit besteht also aus mehreren Elementen, die es zu unterscheiden gilt:</p>
	<ul>
	<li><b>Kreditlaufzeit</b>: Gibt in Monaten an, wie lange der Kredit läuft. Jeden Monat wird eine Ratenzahlung fällig. Am Ende der Laufzeit ist der Kredit bei pünktlichen Zahlungen abbezahlt.</li>
	<li><b>Monatsrate</b>: Beinhaltet die monatliche Rückzahlung sowie einen Teil der anfallenden Zinsen</li>
	<li><b>Zinsen</b>: Diese kommen zur Rückzahlung dazu, sind also die eigentlichen Kreditkosten</li>
	<li><b>Zinssatz</b>: Dieser gibt in % an, wie hoch die Mehrkosten des Kredits für den Kreditnehmer sind (darf 10% nicht übersteigen).</li>
	</ul>
	<p>Wir von Multicrédit garantieren unseren Kunden den bestmöglichen Zinssatz für ihre Kreditwünsche in Abhängigkeit von Ihrer Situation!</p>

