		<p>L'incertitude liée au paiement des clients peut mener certaines entreprises à des difficultés financières, tout particulièrement dans les gestion des liquidités à court terme. Le factoring se présente alors comme une solution visant à <b>assurer un paiement immédiat</b> pour toute facture émise envers la clientèle et d'éliminer ainsi le facteur d'incertitude lié au paiement des clients. Spécialisés dans les solutions pour PME et pour indépendants, nous pouvons vous proposer des offres adaptées.</p>
		<h2>Faire appel à un factor</h2>
		<p>Le rôle du factor, lors de la mise en place d'un contrat de factoring, consiste à <b>verser une avance à l'entreprise pour toute facture émise envers la clientèle</b> (clientèle B2B uniquement). Plus concrètement, le factor avance à l'entreprise un montant équivalent à 90% du montant facturé, ceci dans un délai de 1 - 2 jours ouvrables maximum. Les 10% restants sont alors reversés au paiement du client. De cette manière:</p>
		<ul>
			<li>Les montants ne sont pas immobilisés car en attente du paiement des clients</li>
			<li>L'entreprise bénéficie de liquidités immédiates même en cas de retard de paiement de la clientèle</li>
		</ul>
		<h2>Le service de recouvrement</h2>
		<p>Complémentaire au factoring, le recouvrement consiste à externaliser la gestion des créances échues à un spécialiste qui se chargera d'effectuer les démarches pour récupérer les montants échus. Ce spécialiste aura ainsi pour rôle de relancer les clients n'ayant pas payé, de chercher des arrangements au besoin, et même d'entamer des procédures juridiques si nécessaire. Cette solution permet d'économiser non seulement du temps, mais aussi sur les coûts, le tout en bénéficiant de l'appui d'un spécialiste.</p>
		<h2>Assurance-crédit</h2>
		<p>Enfin, l'assurance-crédit peut venir compléter les deux techniques précédentes pour <b>assurer l'entreprise contre le défaut de paiement des clients</b>. Plus spécifiquement, il s'agit d'une couverture garantissant une indemnisation en cas d'impossibilité ou de refus de paiement d'une facture.</p>
