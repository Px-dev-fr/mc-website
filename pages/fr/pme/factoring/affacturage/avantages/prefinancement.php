		<p>Pour les entreprises proposant la vente de bien ou de services à crédit, les montants facturés qui ne sont pas encore encaissés sont considérés comme des <i>actifs circulants</i>: à savoir une somme pour le moment immobilisée, mais qui pourra être mobilisée sous la forme de liquidités dans un certain délai. Le préfinancement de factures vise à réduire, voir éliminer ce délai en permettant de transformer une facture émise en liquidités sans avoir à attendre le paiement du client.</p>
		<h2>Fonctionnement</h2>
		<p>Le préfinancement de factures, aussi appelé affacturage ou factoring, est un service qui fait appel à <i>un factor</i> avec lequel un contrat est passé. Ce factor aura alors pour rôle de préfinancer toute facture émise envers la clientèle. Plus spécifiquement:</p>
		<ul>
			<li>La société continue d'émettre ses factures comme d'habitude envers sa clientèle. Seule la référence de paiement change, le factor devenant le bénéficiaire.</li>
			<li>Lors de l'envoi d'une facture à la clientèle, une copie est envoyée au factor. Celui-ci verse alors à l'entreprise un montant correspondant à <b>90% du montant facturé dans un délai de 1-2 jours ouvrables seulement</b>.</li>
			<li>Lors du paiement du client (à 30 ou 90 jours), le factor verse alors l'appoint (les 10% restants) à l'entreprise.</li>
			<li>Seules les factures B2B (envoyées à des clients qui sont eux-même d'autres entreprises) peuvent être préfinancées par le factor.</li>
		</ul>
		<h2>Avantages</h2>
		<p>Le préfinancement de factures est doublement avantageux pour les sociétés qui font appel à ce type de services. En effet, cette technique de factoring permet non seulement <b>d'assurer des entrées régulières en liquidités</b> tout en protégeant contre les retards de paiement de la clientèle. En effet:</p>
		<ul>
			<li>L'entreprise bénéficie d'entrées immédiates en liquidités dès l'émission d'une facture. Des ressources alors considérées comme immobilisées sont immédiatement accessibles.</li>
			<li>L'entreprise est en outre protégée en cas de retard de paiement de la clientèle.</li>
		</ul>
