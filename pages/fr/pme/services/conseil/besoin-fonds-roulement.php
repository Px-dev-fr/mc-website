		<p>Le besoin en fonds de roulement est un indicateur de la santé financière à court terme d'une entreprise et représente le bilan entre les dépenses et encaissements prévus à court terme.  Abrégé BFR, cet indicateur permet de mieux appréhender les difficultés prochaines d'une société et de mieux cibler les problèmes afin de trouver des solutions en cas de manque de liquidités.</p>
		<h2>Calculer le BFR</h2>
		<p>De point de vue formel, le besoin en fonds de roulement (BFR) peut se calculer comme la différence entre l'actif circulant et le passif circulant d'une société. Ainsi:</p>
		<p style="text-align:center;font-weight:bold;">BFR = Actif circulant - Passif circulant</p>
		<p>Ce calcul fait appel aux notions d'actif et passif circulant qui représentent les encaissements et dépenses prévus à court terme:</p>
		<ul>
			<li><b>L'actif circulant</b> représente les montants encore non-encaissés: créances clientèles encore impayées, stocks de marchandises, voir d'éventuels autres éléments (réserves de devises, ...).</li>
			<li><b>Le passif circulant</b> représente <i>l'ensemble des dettes dont le dénouement est prévu à court terme</i>: créances dues à des fournisseurs ou aux institutions et administrations (taxes, factures, charges diverses).</li>
		</ul>
		<h2>Les solutions pour faire face aux problèmes de BFR</h2>
		<p>Pas toujours connus des indépendants et petites entreprises, de nombreux produits financiers existent pour les sociétés qui souhaitent améliorer leur bilan entre encaissements et dépenses. Ces solutions adaptables peuvent mener vers une amélioration du BFR. Par exemple:</p>
		<ul>
			<li><b>Le factoring</b> permet de diminuer le délai entre la facturation et l'encaissement grâce au préfinancement de factures</li>
			<li>Faire appel à <b>un service de recouvrement</b> permet d'améliorer la récupération des montants échus.</li>
			<li><b>Le reverse factoring</b> permet de négocier ses prix d'achats avec des fournisseurs</li>
			<li><b>La vente de créances</b> entraîne une amélioration immédiate des liquidités de l'entreprise</li>
			<li><b>Le crédit PME</b> permet de palier à un manque temporaire de trésorerie</li>
			<li><b>Le leasing PME</b> permet l'acquisition de biens et de matériel sans avoir à puiser dans sa trésorerie</li>
			<li>... De nombreux autres produits existent sur le marché et permettent en règle général d'améliorer la situation financière d'une société!</li>
		</ul>
