		<p>Pour toute demande de crédit, il faut nous fournir vos <b>3 dernières fiches salaires</b> ainsi qu'une <b>copie recto-verso d'une pièce d'identité</b> (carte d'indentité suisse, permis de travail B, C, L, G ou carte de légitimation. Pour les indépendants le <b>dernier avis de taxation des impôts</b> est nécessaire. </p>
		<h3>2. Faut-il vivre en Suisse pour obtenir un crédit?</h3>
		<p>Les crédits sont en principe accordés au personnes domiciliées en Suisse ainsi qu'aux frontaliers travaillant en Suisse.</p>
		<h3>3. J'ai déjà un crédit en cours, est-t'il possible d'avoir un supplément d'argent?</h3>
		<p>Oui, il est tout à fait possible d'obtenir un crédit même si vous en avez déjà un autre en cours. C'est ce qu'on appelle un rachat de crédit.</p>
		<h3>4. Dois-je justifier la raison pour laquelle je demande un crédit?</h3>
		<p>Non, absolument pas. Vous êtes libre de faire ce que vous souhaitez avec l'argent de votre crédit.</p>
		<h3>5. Quel montant maximum puis-je obtenir?</h3>
		<p>Le montant du crédit dépend de vos revenus et de votre situation (marié, divorcé, enfants à charge) et sont variables d'une personne à l'autre. Au maximum, nous accordons des prêts à hauteur de 300'000 Chf.</p>
		<h3>6. Combien de temps faut-il pour recevoir l'argent sur mon compte?</h3>
